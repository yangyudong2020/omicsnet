class FeatureNet(nn.Module):
    def __init__(self, transformer_width=512, transformer_heads=2, transformer_layers=18, embed_dim=512):
        super(FeatureNet, self).__init__()

        self.init_embed = clip_content[0]
        self.context_length = clip_content[1]
        self.fc = [nn.Linear(self.init_embed, 1024), nn.LeakyReLU(), nn.Linear(1024, 512)]
        self.fc = nn.Sequential(*self.fc)
            
        self.fc = nn.Linear(self.init_embed, transformer_width)
        self.transformer_width = transformer_width
        self.positional_embedding = nn.Parameter(
            torch.empty(self.context_length, transformer_width))
        self.transformer = Transformer(
            width=transformer_width,
            layers=transformer_layers,
            heads=transformer_heads,
            attn_mask=self.build_attention_mask()
        )
        self.text_projection = nn.Parameter(torch.empty(transformer_width, embed_dim))
        self.ln_final = LayerNorm(transformer_width)
    
    def build_attention_mask(self):
        # lazily create causal attention mask, with full attention between the vision tokens
        # pytorch uses additive attention mask; fill with -inf
        mask = torch.empty(self.context_length, self.context_length)
        mask.fill_(float("-inf"))
        mask.triu_(1)  # zero out the lower diagonal
        return mask    
    

    @property
    def dtype(self):
        # return list(self.fc.parameters())[0].dtype
        return self.fc.weight.dtype
    
    def forward(self, x):
        # print('feature net input x is', x.shape)
        # print('positional_embedding', self.positional_embedding.type(self.dtype).shape)
        # print('input clip shape', x.shape)
        x = x.reshape(-1, self.init_embed)
        x = self.fc(x)
        # print('x shape is', x.shape, self.context_length, self.transformer_width)
        x = x.reshape(-1, self.context_length, self.transformer_width)
        x = x + self.positional_embedding.type(self.dtype)
        x = x.permute(1, 0, 2)  # NLD -> LND
        x = self.transformer(x)
        # print('transformer shape', x.shape)
        x = x.permute(1, 0, 2)  # LND -> NLD
        x = self.ln_final(x).type(self.dtype)
        # print('ln_final shape', x.shape)

        # x.shape = [batch_size, n_ctx, transformer.width]
        # take features from the eot embedding (eot_token is the highest number in each sequence)
        x = x[torch.arange(x.shape[0]), 0] @ self.text_projection

        return x